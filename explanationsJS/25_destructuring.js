// Destructuring

// O Destructuring é uma maneira conveniente de extrair valores de arrays ou propriedades de objetos em suas próprias variáveis.

let [a, b, c] = [1, 2, 3];
console.log(a); // 1
console.log(b); // 2
console.log(c); // 3

let {nome, idade} = {nome: 'João', idade: 25};
console.log(nome); // João
console.log(idade); // 25
