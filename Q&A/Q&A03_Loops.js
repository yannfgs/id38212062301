/* 

-- Pergunta 3: O que são e como funcionam os loops em JavaScript?

Loops são usados para executar repetidamente um bloco de código enquanto uma condição especificada é verdadeira. Os tipos mais comuns de loops em JavaScript são `for`, `while` e `do...while`.

*/

// Exemplo com for:
for (let i = 0; i < 5; i++) {
    console.log(i); // Este código exibe os números de 0 a 4 no console
}

// `let i = 0;` é a inicialização. Aqui declaramos a variável `i` e inicializamos com 0.
// `i < 5;` é a condição. O loop continuará enquanto `i` for menor que 5.
// `i++` é o incremento. Após cada iteração do loop, incrementamos `i` em 1.