/*

-- Pergunta 2: Como funcionam as estruturas condicionais em JavaScript?

Estruturas condicionais são usadas para realizar diferentes ações com base em diferentes condições. O `if` é a estrutura condicional mais básica em JavaScript. Você também pode usar `else` e `else if` para testar múltiplas condições.

*/

// Vamos ver um exemplo
let idade = 20;

if (idade >= 18) {
  console.log("Você é maior de idade."); // Esta linha é executada se 'idade' for maior ou igual a 18
} else {
  console.log("Você é menor de idade,"); // Esta linha é executada se 'idade' for menor que 18
}
