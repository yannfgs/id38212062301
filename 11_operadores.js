// JavaScript tem vários tipos de operadores, incluindo operadores aritméticos, de comparação e lógicos.

let a = 10;
let b = 20;

console.log(a + b); //  30   (adição)
console.log(a - b); //  -10  (subtração)
console.log(a * b)  //  200  (multiplicação)
console.log(a / b); //  0.5  (divisão)
console.log(a % b); //  10   (módulo)

console.log(a == b);    //  false   (igualdade)
console.log(a != b);    //  true    (desigualdade)
console.log(a < b);     //  true    (menor que)
console.log(a > b);     //  false   (maior que)

console.log(a && b);    //  20      (E lógico)
console.log(a || b);    //  10      (OU lógico)
console.log(!a);        //  false   (NÃO lógico)